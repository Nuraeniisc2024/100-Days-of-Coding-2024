package isc;

public class Day58 {
    public static void main(String[] args) {
        int ku = 5;

        while (ku >= 1) {
            System.out.println("Nilai ku: " + ku);
            ku--;  // Menambah nilai ku sebanyak 1
        }

        System.out.println("Nilai akhir ku: " + ku);
    }
}
